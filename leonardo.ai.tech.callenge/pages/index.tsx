import Head from "next/head";
import { Inter } from "next/font/google";
import createApolloClient from "@/util/apollo-client";
import { gql } from "@apollo/client";
import { useContext, useEffect, useState } from "react";
import { useRouter } from "next/router";
import { getSession } from "next-auth/react";
import { UserContext } from "@/context/UserContext";
import {
  Button,
  Card,
  CardBody,
  CardFooter,
  CardHeader,
  Center,
  Heading,
  Text,
} from "@chakra-ui/react";

const inter = Inter({ subsets: ["latin"] });

type PropType = { countries: Array<any> };

export default function Home({ countries }: PropType) {
  const [isLoading, setIsLoading] = useState(true);
  const router = useRouter();

  const { user, setUser } = useContext<UserContextType>(UserContext);

  useEffect(() => {
    getSession().then((session) => {
      if (!session) {
        router.replace("/login");
      } else {
        setIsLoading(false);
        setUser(session as unknown as User);
      }
    });
  }, [router, setUser]);

  if (isLoading) {
    return <p>Loading...</p>;
  }

  const hasProfile = user?.username || user?.title;

  return (
    <>
      <Head>
        <title>Leonardo.AI Challenge</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Center height="calc(100vh - 75px)">
        <Card minWidth="20rem">
          <CardHeader>
            <Heading size="md">Profile</Heading>
          </CardHeader>
          <CardBody>
            {hasProfile ? (
              <>
                <Text>
                  Username: <Text as="b">{user?.username ?? "N/A"}</Text>
                </Text>
                <Text>
                  Job Title: <Text as="b">{user?.title ?? "N/A"}</Text>
                </Text>
              </>
            ) : (
              <Text>Not Yet Filled</Text>
            )}
          </CardBody>
          <CardFooter>
            <Button>Update</Button>
          </CardFooter>
        </Card>
      </Center>
    </>
  );
}

